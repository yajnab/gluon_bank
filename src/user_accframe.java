       Character k=Character.toUpperCase(c);
       if(k.equals('M')||k.equals('F')){
	   l=k;
       }
       else if((c==KeyEvent.VK_BACK_SPACE) || (c==KeyEvent.VK_DELETE)) {
	   evt.consume();
       }
       else{
	   evt.consume();
	   JOptionPane.showMessageDialog(null, "Wrong Sex input.M or F is only supported.");   
       }
       
       
